function appendToJson(filename: string, newEntries: any[]) {
  const filepath = path.join(DATA_DIR, filename);
  const existing = JSON.parse(fs.readFileSync(filepath, 'utf-8'));
  
  const existingIds = new Set(existing.map((e: any) => e.id));
  const deduped = newEntries.filter(e => !existingIds.has(e.id));
  
  if (deduped.length < newEntries.length) {
    console.log(`⚠️  Skipped ${newEntries.length - deduped.length} duplicate entries in ${filename}`);
  }
  
  const merged = [...existing, ...deduped];
  fs.writeFileSync(filepath, JSON.stringify(merged, null, 2), 'utf-8');
  console.log(`✅ ${filename}: Added ${deduped.length} entries (${existing.length} → ${merged.length} total)`);
}

